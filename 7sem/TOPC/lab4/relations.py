def warchall(a_matrix):
    aPlus_matrix = a_matrix
    n = len(a_matrix)
    
    for i in range(n):
        for j in range(n):
            for k in range(n):
                aPlus_matrix[j, k] = aPlus_matrix[j, k] or (aPlus_matrix[j, i] and aPlus_matrix[i, k])
            
    return aPlus_matrix

